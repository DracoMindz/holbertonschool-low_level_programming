/**
 * int _islower(int c) - checks for lowercase charaters
 *
 * Description: look for lowercase alphabet
 *
 * Return: return 1 if true return 0 if false
 */

int _islower(int c)
{

		if (c >= 'a' && c <= 'z')
		{
			return (1);
		}
		else
		{
			return (0);
		}
		return (0);
}
